void main() {
  print('Hello, World!');
}

// void : C'est un mot-clé technique qui indique que cette fonction fait son travail mais ne renvoie aucun résultat à la fin (elle est "vide").

// main() : C'est le nom de la fonction principale. Les parenthèses () indiquent qu'il s'agit d'une fonction qui peut recevoir des instructions.

// { } : Les accolades délimitent le "corps" de la fonction. Tout le code situé entre ces accolades sera exécuté.

// print('...') : C'est une commande intégrée qui permet d'afficher du texte à l'écran (dans la console).

// ; : Le point-virgule marque la fin d'une instruction. En Dart, il est obligatoire après chaque action.

// Tape la commande suivante pour exécuter ton script : dart main.dart